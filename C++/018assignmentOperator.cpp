#include<iostream>

using namespace std;


int main(){

    int a = 10;
    // +=
    a += 2;
    cout << a << endl;
    // -=
    a = 10;
    a -= 2;
    cout << a << endl;
    // *=
    a = 10;
    a *= 2;
    cout << a << endl;
    // /=
    a = 10;
    a /= 2;
    cout << a << endl;
    // %=
    a = 10;
    a %= 2;
    cout << a << endl;

    return 0;
}